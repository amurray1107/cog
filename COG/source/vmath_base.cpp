#include <COG/vmath_base.h>
#include <math.h>

namespace cog{
  
  F32 _sqrt(F32 x)
  {
    return ::sqrtf(x);
  }
  
  F32 floor(F32 x)
  {
    return ::floorf(x);
  }
  
  F32 acos(F32 x)
  {
    return ::acosf(x);
  }
  
  F32 sin(F32 x)
  {
    return ::sinf(x);
  }
  
  F32 cos(F32 x)
  {
    return ::cosf(x);
  }
  
  F32 tan(F32 x)
  {
    return ::tanf(x);
  }
  
  
  
}
