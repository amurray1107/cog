#include <cog/vmath_base.h>
#include <math.h>

namespace cog{
  
  float _sqrt(float x)
  {
    return ::sqrtf(x);
  }
  
  float floor(float x)
  {
    return ::floorf(x);
  }
  
  float acos(float x)
  {
    return ::acosf(x);
  }
  
  float sin(float x)
  {
    return ::sinf(x);
  }
  
  float cos(float x)
  {
    return ::cosf(x);
  }
  
  float tan(float x)
  {
    return ::tanf(x);
  }
  
}
